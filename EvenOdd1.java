package basicProblem;

public class EvenOdd1 {

     static void EvenOdd(int num) {
         if(num%2==0) {
        	 
        	 System.out.println(num+ " - Even Number");
         }else {
        	 System.out.println(num+ " - Odd Number");
         }
		
		
		
	}
	
	public static void main(String[] args) {
		EvenOdd(12);
	}
}
